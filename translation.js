let currentLanguage = localStorage.getItem("queencoLanguage") || "en";

function t(key) {
  return APP_TRANSLATIONS[currentLanguage]?.[key] || APP_TRANSLATIONS.en[key] || key;
}

function applyTranslations() {
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach(element => {
    const key = element.dataset.i18n;
    element.textContent = t(key);
  });

  document.querySelectorAll("[data-i18n-html]").forEach(element => {
    const key = element.dataset.i18nHtml;
    element.innerHTML = t(key);
  });

  const selector = document.getElementById("languageSelect");
  if (selector) selector.value = currentLanguage;
}

function setLanguage(language) {
  currentLanguage = APP_TRANSLATIONS[language] ? language : "en";
  localStorage.setItem("queencoLanguage", currentLanguage);
  applyTranslations();

  if (typeof renderPointsRace === "function") {
    renderPointsRace();
  }
}

function initLanguageSelector() {
  const selector = document.getElementById("languageSelect");
  if (!selector) return;

  selector.value = currentLanguage;
  selector.addEventListener("change", event => setLanguage(event.target.value));
}
